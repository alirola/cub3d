#include "cub3d.h"

void	free_tptr(char ***tmp)
{
	int	i;

	i = 0;
	if (*tmp != NULL)
	{
		while((*tmp)[i])
		{
			free((*tmp)[i]);
			(*tmp)[i] = NULL;
		}
		free(*tmp);
		*tmp = NULL;
	}
}
